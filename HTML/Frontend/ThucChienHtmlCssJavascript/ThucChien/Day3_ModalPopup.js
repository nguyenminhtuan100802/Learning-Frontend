function visiblePopUp() {
  document.getElementById("box_pop_up").style.opacity = "1";
  document.getElementById("box_pop_up").style.transform = "translateY(50px)";

}

function hiddenPopUp() {
  document.getElementById("box_pop_up").style.opacity = "0";
  document.getElementById("box_pop_up").style.transform = "translateY(-50px)";
}
